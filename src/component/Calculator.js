function add(a, b) {
    const add = a + b;
    return add;
}

function multi(a, b) {
    const multi = a * b;
    return multi;
}


function div(a, b) {
    const div = a / b;
    return div;
}

function sub(a, b) {
    const sub = a - b;
    return sub;
}

export { add, multi, div, sub };